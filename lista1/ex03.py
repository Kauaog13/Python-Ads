# 3. Faça um programa que inicialize uma lista de compras com 5 itens diferentes e exiba todos utilizando um laço de repetição.


# Inicializa uma lista de compras com 5 itens diferentes
compras = ["Arroz", "Feijão", "Macarrão", "Queijo", "Ovo"]  # Lista de compras

# Utiliza um laço de repetição para exibir todos os itens da lista
for item in compras:  # Itera sobre cada item da lista
    print(item)  # Exibe o item atual